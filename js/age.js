let age = 60
if(age <= 1) {
    console.log(`This ${age} categorized in infant.`)
}else if(age > 1 && age <= 3){
    console.log(`This ${age} categorized in toddler.`)
}else if(age > 3 && age <= 9){
    console.log(`This ${age} categorized in child.`)

}else if(age > 9 && age <= 12){
    console.log(`This ${age} categorized in preteen.`)
}else if(age > 12 && age <= 18){
    console.log(`This ${age} categorized in teen.`)
}else if(age > 18 && age <= 25){
    console.log(`This ${age} categorized in young adult.`)
}else{
    console.log(`This ${age} categorized in adult.`)

}



